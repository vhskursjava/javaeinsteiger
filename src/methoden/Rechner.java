package methoden;

public class Rechner {

	/**
	 * Addiert zwei Ganzzahlen und gibt das Ergebnis zurück. 
	 * @param a Operand 1
	 * @param b Operand 2
	 * @return Das Ergebnis von a addiert mit b.
	 */
	public static int addieren(int a, int b) {
		return a + b;
	}
	
	public static double dividieren(double a, double b) {
		return a / b;
	}
	
	public static double multiplizieren(double a, double b) {
		return a * b;
	}
}
